/**
 * Retourne le référentiel FWB pour une matière et un niveau donnés
 */
export function getReferentiel(subject: string, niveau?: string): string {
  const ref = REFERENTIELS_PAR_MATIERE[subject.toLowerCase()] ?? "";
  if (!ref) return "";

  const header = `\n## RÉFÉRENTIEL FWB APPLICABLE — ${subject.toUpperCase()}${niveau ? ` — ${niveau}` : ""}\n`;

  // Filtre pour ne garder que la section du bon niveau si spécifié
  if (niveau && (niveau.includes("S") || niveau.includes("s"))) {
    const niveauKey = niveau.toUpperCase().replace(/^\d/, (d) => `${d}e `);
    // Retourner le référentiel complet — le niveau sera précisé dans le prompt
    return header + ref;
  }

  return header + ref;
}

